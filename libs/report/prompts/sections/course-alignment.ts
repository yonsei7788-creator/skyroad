/** 섹션 7: 권장과목 이수 분석 (courseAlignment) */

import type { ReportPlan } from "../../types.ts";

export interface CourseAlignmentPromptInput {
  recommendedCourseMatch: string;
  competencyExtraction: string;
  studentProfile: string;
  studentGrade: number;
  gradingSystem: "5등급제" | "9등급제";
  isMedical?: boolean;
  isGyogwaOnly?: boolean;
}

const PLAN_SPECIFIC: Record<ReportPlan, string> = {
  lite: `## 플랜별 출력: 기본
- 목표 계열 + 이수율 + 이수/미이수 상세 + 미이수 영향 분석 + 이수 전략
- medicalRequirements 필드는 출력하지 않습니다.`,
  standard: `## 플랜별 출력: 상세
- Lite의 모든 항목
- 의·치·한·약·수 계열 대학별 요구사항 매칭(medicalRequirements, 해당 계열 학생만)

⚠️ **분량 제한 (반드시 준수)**:
- courses 배열은 **최대 5개**입니다. 6개 이상 절대 출력하지 마세요.
- 각 course의 관련 설명(reason 등)은 **80자 이내**로 작성합니다.
- missingCourseImpact는 **100자 이내**, recommendation은 **100자 이내**로 작성합니다.`,
  premium: `## 플랜별 출력: 정밀
- Standard와 동일

⚠️ **분량 제한 (반드시 준수)**:
- courses 배열은 **최대 6개**입니다. 7개 이상 절대 출력하지 마세요.
- 각 course의 관련 설명(reason 등)은 **80자 이내**로 작성합니다.
- missingCourseImpact는 **100자 이내**, recommendation은 **100자 이내**로 작성합니다.
- ⚠️ 이 섹션은 **A4 1페이지 이내**에 들어와야 합니다. 간결하게 작성하세요.`,
};

export const buildCourseAlignmentPrompt = (
  input: CourseAlignmentPromptInput,
  plan: ReportPlan
): string => {
  const curriculumWarning =
    input.gradingSystem === "5등급제"
      ? `⚠️ **교육과정 버전: 2022 개정 교육과정 (5등급제)**
- 이 학생은 5등급제 적용 학생입니다. 과목명은 반드시 2022 개정 교육과정 기준으로 작성하세요.
- 2015 교육과정 과목명을 절대 사용하지 마세요:
  - ❌ 물리학Ⅰ/Ⅱ → ✅ 물리학, 역학과 에너지, 전자기와 양자
  - ❌ 화학Ⅰ/Ⅱ → ✅ 화학, 물질과 에너지, 화학 반응의 세계
  - ❌ 생명과학Ⅰ/Ⅱ → ✅ 생명과학, 세포와 물질대사, 생물의 유전
  - ❌ 지구과학Ⅰ/Ⅱ → ✅ 지구과학, 지구시스템과학, 행성우주과학
  - ❌ 수학Ⅰ/Ⅱ, 미적분 → ✅ 대수, 미적분Ⅰ, 미적분Ⅱ
  - ❌ 사회·문화, 사회문화 → ✅ 사회와 문화
  - ❌ 정치와 법 → ✅ 정치, 법과 사회
  - ❌ 한국지리 → ✅ 한국지리 탐구
  - ❌ 세계지리 → ✅ 세계시민과 지리`
      : `⚠️ **교육과정 버전: 2015 개정 교육과정 (9등급제)**
- 이 학생은 9등급제 적용 학생입니다. 과목명은 반드시 2015 개정 교육과정 기준으로 작성하세요.`;

  const medicalCourseContext = input.isMedical
    ? `
## ⚠️ 의·치·한·약·수 계열 과목 선택 특화 가이드 (반드시 적용)

이 학생은 의·치·한·약·수 계열(의·치·한·약·수) 지원 학생입니다.

### 의·치·한·약·수 핵심 과목 이수 기준
- **수학**: 미적분(2015) / 미적분Ⅰ·Ⅱ(2022)는 의·치·한·약·수에서 사실상 필수입니다. 확률과 통계만 이수한 경우 학종에서 "전공 관련 교과 이수 노력 부족"으로 평가될 수 있습니다.
- **과학**: 생명과학Ⅱ·화학Ⅱ(2015) / 세포와 물질대사·물질과 에너지(2022) 등 심화 과학 이수 여부가 의·치·한·약·수 학종의 핵심 평가 요소입니다.
- **영어**: 의·치·한·약·수에서 영어 1등급은 사실상 필수 조건입니다. 과목 이수 분석에서 영어 성취도도 함께 언급하세요.

### 사탐 의·치·한·약·수 현상과 과목 선택 전략 (2026학년도 기준)
최근 의·치·한·약·수 계열에서 사회탐구/확률과 통계 선택자가 증가하고 있습니다:
- 의예과: 사탐 응시자 9.3%
- 한의예과: 74.8% (사실상 사탐 경쟁 중심)
- 수의예과: 40.5%
- 약학계열: 23.8%

#### 과탐 선택의 장점
- 대부분의 의·치·한·약·수 대학이 과탐 응시자에게 가산점 부여
- 미적분·기하 + 과탐 조합이 전통적으로 의·치·한·약·수 지원의 정석
- 학종에서 "전공 관련 교과 이수 노력"으로 긍정 평가

#### 사탐/확률과 통계 선택의 장점과 위험
- **장점**: 과탐 대비 안정적인 고득점(높은 백분위) 확보 가능. 과탐은 한 문제 차이로 등급·백분위가 크게 변동
- **위험**: 확률과 통계는 상위권 점수 집중도가 높아 극상위 확보 부담. 표준점수 최고점이 미적분·기하 대비 낮은 경우가 많음
- **전략적 판단**: 단순 과목 난이도가 아닌 **표준점수 구조 + 응시 집단 분포 + 목표 대학의 반영 방식**을 종합 고려해야 함

#### ⚠️ recommendation 필드에 반드시 포함할 문장 (누락 시 품질 실패)

아래 항목 중 해당하는 것을 recommendation 필드에 **그대로** 포함하세요:

1. **고1~2 학생 (필수)**: 다음 문장을 recommendation에 반드시 포함:
   "정시 과목 선택 전략도 중요합니다. 2026학년도 기준 의·치·한·약·수 사탐 응시자 비율은 의예과 9.3%, 한의예과 74.8%, 수의예과 40.5%, 약학 23.8%로, 사탐 의·치·한·약·수 현상이 확대되고 있습니다. 단, 확률과 통계는 상위권 점수 집중도가 높고 표준점수 최고점이 미적분·기하 대비 낮으므로, 단순 난이도가 아닌 표준점수 구조 + 응시 집단 분포 + 목표 대학 반영 방식을 종합 고려하여 선택해야 합니다."

2. **과탐 미이수 + 의·치·한·약·수 지원**: "학종에서 전공 관련 교과 이수 노력 평가에서 불리할 수 있습니다. 과탐 이수를 적극 권장합니다." 반드시 추가

3. **한의예과 지원**: "한의예과는 사탐 응시자가 74.8%로 압도적이므로 사탐 선택이 오히려 유리할 수 있습니다." 반드시 추가

`
    : "";

  const gyogwaOnlyContext = input.isGyogwaOnly
    ? `## ⛔ 교과전형 전용 (이 규칙이 다른 모든 지시보다 우선)
이 학생은 모든 희망대학이 학생부교과전형입니다.
- "학종에서 '교과 이수 노력' 평가", "학종의 핵심 평가 요소" 등의 표현을 사용하지 마세요.
- 교과 이수 분석은 "교과전형에서 반영되는 과목 구성" 관점으로 작성하세요.

`
    : "";

  return `${gyogwaOnlyContext}${curriculumWarning}
${medicalCourseContext}
## 작업
학생의 목표 계열 대비 권장과목 이수 상태를 분석하고 영향을 평가하세요.

## 이 섹션의 역할
목표 계열이 요구하는 과목 대비 **이수 현황과 구조적 공백**을 분석합니다.
각 과목의 성적 추이는 academicAnalysis, 세특 내용 분석은 subjectAnalysis에서 다루므로, 여기서는 이수 여부와 학과 매칭률에 집중합니다.
## 서술 관점: 교육과정 설계자
이 섹션은 **이수 과목과 필요 과목의 구조적 매칭**을 분석합니다. 이수/미이수 현황과 그 입시 영향을 중심으로 서술하세요.
- "~과목 이수로 ~요건 충족", "~과목 미이수로 ~에 공백 발생" 등 과목 구조 분석 어투를 사용하세요.
- 다른 섹션에서 이미 사용한 표현이나 문장 구조를 반복하지 마세요.

## ⚠️ 분석 방향 원칙
- 아래 "권장과목 매칭 데이터"는 **생기부 기반으로 감지된 강점 계열의 권장과목**입니다.
- **courses 배열은 반드시 아래 매칭 데이터의 requiredCourses 목록을 그대로 사용하세요.** AI가 임의로 과목을 추가하거나 변경하면 안 됩니다.
- **targetMajor는 아래 역량 추출 결과의 detectedMajorGroupLabel 값을 그대로 사용하세요. (예: "의학 계열", "약학 계열", "컴퓨터/AI/소프트웨어 계열")**
- matchRate도 아래 매칭 데이터의 값을 그대로 사용하세요.

## 입력 데이터

### 권장과목 매칭 데이터 (생기부 기반 — 코드 전처리 결과)
⚠️⚠️⚠️ **아래 데이터가 이 섹션의 핵심 입력입니다. courses 배열은 반드시 이 데이터의 requiredCourses를 그대로 사용하세요.**
- requiredCourses → courses 배열의 과목 목록
- takenCourses → status: "이수"
- missingCourses → status: "미이수"
- matchRate → 출력의 matchRate
${input.recommendedCourseMatch}

### 역량 추출 결과
${input.competencyExtraction}

⚠️ **targetMajor**: 위 역량 추출 결과의 detectedMajorGroup 값을 그대로 사용하세요.

### 학생 프로필
${input.studentProfile}

### 학년별 분석 방향
- 학생 학년: ${input.studentGrade}학년
${
  input.studentGrade >= 3
    ? `- 이 학생은 3학년으로 **과목 선택이 완료된 상태**입니다.
- "남은 기간에 이수하라"는 조언은 부적절합니다.
- 현재 이수한 과목 기준으로 입시 영향만 분석하세요.
- 미이수 과목이 있다면: "이미 선택 기회가 지났으므로, 면접에서 해당 과목 미이수 사유를 설명할 준비가 필요합니다"와 같은 방향으로 분석하세요.
- recommendation 필드는 이수 전략 대신 "미이수 상태에서의 대응 전략"을 작성하세요.`
    : `- 이 학생은 ${input.studentGrade}학년으로 남은 학기에 추가 이수가 가능합니다.
- 미이수 과목에 대해 구체적인 이수 전략을 제안하세요.`
}

## 출력 JSON 스키마

중요: courses 배열의 각 요소는 반드시 아래와 같은 완전한 객체여야 합니다.

{
  "sectionId": "courseAlignment",
  "title": "과목 적합도",
  "targetMajor": "사회과학",
  "matchRate": 75,
  "courses": [
    {"course": "정치와법", "status": "이수", "importance": "필수"},
    {"course": "사회·문화", "status": "이수", "importance": "필수"},
    {"course": "경제", "status": "미이수", "importance": "권장"}
  ],
  "missingCourseImpact": "경제 과목 미이수는 사회과학 계열 진학 시...",
  "recommendation": "3학년에서 경제 과목 이수를 권장합니다..."
}

## 출력 지시

### 목표 계열 (targetMajor)
- 역량 추출 결과의 detectedMajorGroupLabel 값을 그대로 사용합니다. (예: "의학 계열", "약학 계열")

### 권장과목 이수율 (matchRate)
- 권장과목 매칭 데이터의 matchRate 값을 그대로 사용합니다.

### 이수/미이수 상세 (courses)
- ⚠️ **반드시 권장과목 매칭 데이터의 requiredCourses 목록을 그대로 사용하세요.** AI가 과목을 임의로 추가/변경/삭제하면 안 됩니다.
- takenCourses에 포함된 과목 → status: "이수"
- missingCourses에 포함된 과목 → status: "미이수"
- importance: "핵심 권장" 또는 "권장" (⚠️ "필수"라는 표현은 사용하지 마세요.)

### 미이수 과목 영향 분석 (missingCourseImpact)
- 미이수 과목이 입시에 미치는 영향을 구체적으로 서술합니다.
- 특히 학종에서 "교과 이수 노력" 평가에 미치는 영향을 분석합니다.

### 이수 전략 제안 (recommendation)
- 고1~2 학생에 해당하는 경우, 남은 학기에 이수할 과목을 제안합니다.
- 고3이거나 이미 이수 완료인 경우 생략 가능합니다.

## 미이수 과목 이수 전략 (두 가지 시나리오 필수)
미이수 과목에 대해 반드시 **두 가지 시나리오**로 나누어 분석하세요:
- **A. 향후 수강할 경우**: 해당 과목을 앞으로 이수한다면 진로역량과 입시 경쟁력에 어떤 긍정적 변화가 있는지 서술합니다.
- **B. 미이수가 유지될 경우**: 해당 과목 없이 지원할 때의 불이익과 이를 보완할 수 있는 대안 전략을 제시합니다.
⚠️ 단, 졸업생(학생 프로필의 "학생 상태"가 "졸업생")의 경우 A 시나리오는 생략하고 B 시나리오만 작성합니다.

${PLAN_SPECIFIC[plan]}`;
};
