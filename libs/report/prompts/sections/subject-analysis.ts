/** 섹션 10: 교과 세특 분석 (subjectAnalysis) */

import type { ReportPlan } from "../../types.ts";

export interface SubjectAnalysisPromptInput {
  subjectData: string;
  studentProfile: string;
}

const COMPETENCY_TAG_GUIDE = `## 역량 태깅 가이드
사용 가능한 태그:
- 학업역량-학업성취도, 학업역량-학업태도, 학업역량-탐구력
- 진로역량-교과이수노력, 진로역량-교과성취도, 진로역량-진로탐색
- 공동체역량-나눔과배려, 공동체역량-소통및협업, 공동체역량-리더십, 공동체역량-성실성
- 발전가능성-자기주도성, 발전가능성-경험다양성, 발전가능성-성장과정, 발전가능성-창의적문제해결`;

const PLAN_SPECIFIC: Record<ReportPlan, string> = {
  lite: `## 분석 수준: 기본
각 과목에 대해 다음을 출력하세요:
- 평가 등급: excellent / good / average / weak
- 핵심 활동 요약: 주요 활동 1~2개를 2~3줄로 요약
- 평가 코멘트: 강점/약점 간략 서술 (3~4줄)
- 역량 태깅: 해당 세특에서 드러나는 역량 태그 1~3개`,
  standard: `## 분석 수준: 상세
각 과목에 대해 Lite의 모든 항목 + 다음을 추가로 출력하세요:
- 원문 핵심 인용: 세특에서 평가에 중요한 문장 2~3개를 직접 인용
- 상세 평가: 전문 컨설턴트 수준의 구체적 평가 (1문단, 5~8줄)
  - 이 과목의 세특이 왜 좋은지/부족한지 구체적으로 서술
  - "과목다운 세특"인지 판단 (과목 본질에 맞는 탐구/사고가 드러나는지)
  - 입시 사정관이 이 세특을 읽었을 때 어떤 인상을 받을지 분석
- 개선 방향: 3학년 세특에서 보완할 구체적 방향 (2~3줄)
- 개선 예시 문장: 약한 부분이 있다면 "이렇게 서술되었으면 더 좋았을 것"이라는 구체적 예시 1개 제시
- 교과 간 연결성: 다른 과목 세특과 주제적으로 연결/중복되는 부분이 있으면 명시
  - 주제가 반복되는 경우: 심화인지 단순 반복인지 구분하여 명시`,
  premium: `## 분석 수준: 정밀 (문장 단위)
각 과목에 대해 Standard의 모든 항목 + 다음을 추가로 출력하세요:
- 문장 단위 분석: 세특 원문을 문장(또는 의미 단위 문장 그룹)으로 나누어 각각 분석
  - 각 문장에 대해: 원문, 평가 코멘트, 역량 태그, 하이라이트 여부(positive/negative/neutral)
  - 감점 요인 문장에 대해: "이렇게 서술되었으면 좋았을 것"이라는 개선 예시 제시
- 중요도 퍼센트: 이 과목의 세특이 전체 생기부에서 차지하는 평가 중요도 (0~100%)
  - 전공 관련성, 세특 내용의 질, 분량을 종합 고려
- 평가 영향도: high / medium / low (합격 판단에 미치는 영향)

## 문장 단위 분석 예시
원문: "에너지를 생산하기 위해 소비되는 비효율적 구조를 고찰하며, 식물을 활용한 에너지 생산의 가능성을 심도 있게 탐구함."
평가: "'심도 있게 탐구했다'는 서술이 있으나 구체적으로 어떤 내용을 탐구했는지 드러나지 않음. '인공 광합성 시스템과 식물-미생물 연료 전지를 중심으로' 같은 구체적 키워드가 포함되었다면 탐구 깊이를 평가자가 파악할 수 있었을 것."
역량: 학업역량-탐구력
하이라이트: negative`,
};

export const buildSubjectAnalysisPrompt = (
  input: SubjectAnalysisPromptInput,
  plan: ReportPlan
): string => {
  return `## 작업
학생의 세부능력 및 특기사항(세특)을 과목별로 분석하세요.

## 규칙
1. **교과 세특만** 분석합니다. 창체(자율·자치/동아리/진로)는 activityAnalysis에서 별도 분석합니다.
2. 평가는 반드시 원문 내용에 근거합니다.
3. 역량 태깅은 가장 두드러지는 역량 1~3개를 선택합니다.
4. 학년-과목명 형식으로 과목을 식별합니다 (예: "2학년 물리학1").
5. **"과목다운 세특" 관점을 반드시 적용합니다**: 해당 과목의 본질적 특성에 맞는 세특인지 평가합니다.
   - 수학 세특에는 수학적 사고/증명/모델링이, 물리학 세특에는 실험/현상 분석이 드러나야 합니다.
   - 과목 특성과 무관한 단순 소재 활용(예: 물리학 세특인데 자동차 역사 조사)은 감점 요인입니다.
6. **활동 밀도를 분석합니다**: 1500바이트(약 500자) 내 3개 이상 활동 나열은 깊이 부족으로 판단합니다.
   - 활동 수 대비 분량을 확인하고, 밀도가 낮은(=활동 수는 적지만 깊이 있는) 세특을 높이 평가합니다.

${COMPETENCY_TAG_GUIDE}

## 학생 프로필
${input.studentProfile}

## 교과 세특 원문 데이터
${input.subjectData}

${PLAN_SPECIFIC[plan]}`;
};
