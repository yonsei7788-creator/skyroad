/**
 * 카테고리로 학과명 목록을 조회하는 역방향 맵
 */
const _reverseCategoryMap: Map<string, string[]> = new Map();
for (const [dept, cat] of Object.entries(DEPARTMENT_CATEGORY_MAP)) {
  if (!_reverseCategoryMap.has(cat)) _reverseCategoryMap.set(cat, []);
  _reverseCategoryMap.get(cat)!.push(dept);
}

/** 카테고리에 속하는 모든 학과명 반환 */
export const getDepartmentsByCategory = (category: string): string[] =>
  _reverseCategoryMap.get(category) ?? [];

/** 학과명의 카테고리 조회 */
export const getCategoryForDepartment = (
  department: string
): string | undefined => DEPARTMENT_CATEGORY_MAP[department];
