/** 변수 치환 + 조사 자동 처리 */
export const fillTemplate = (
  template: string,
  data: CompetitiveProfilingSection
): string => {
  const majorDir = data.majorDirection;
  const keywordBase = data.keywords.join(", ");
  const keywordFirst = data.keywords[0] ?? "";

  return template
    .replace(/{전공 방향}/g, majorDir)
    .replace(/{키워드 기반}/g, keywordBase)
    .replace(/{키워드 활동}/g, keywordFirst)
    .replace(/\{전공 방향\} 준비/g, `${josa(majorDir, "을", "를")} 준비`)
    .replace(/\{키워드 기반\} 바탕/g, `${josa(keywordBase, "을", "를")} 바탕`)
    .replace(/\{키워드 활동\} 중심/g, `${josa(keywordFirst, "을", "를")} 중심`);
};
